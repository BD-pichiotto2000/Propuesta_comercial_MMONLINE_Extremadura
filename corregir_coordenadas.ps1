$html = [System.IO.File]::ReadAllText('C:\Users\pichi\extremadura_mapa_zonas.html')
$actualizados = 0

# Regex para encontrar cada entrada del array municipios
$pattern = "(\{prov:'([^']+)',\s*mun:'([^']+)',\s*cp:'[^']+',\s*dist:\d+,\s*zona:'[^']+',\s*lat:)([\d\.\-]+)(,\s*lon:)([\d\.\-]+)(,)"
$ms = [regex]::Matches($html, $pattern)

foreach ($m in $ms) {
    $prov = $m.Groups[2].Value
    $mun  = $m.Groups[3].Value
    $latOld = [double]$m.Groups[4].Value
    $lonOld = [double]$m.Groups[6].Value

    # Intentar buscar con acento y sin acento
    $key = "$prov|$mun"
    $c = $null
    if ($coords.ContainsKey($key)) {
        $c = $coords[$key]
    } else {
        # Buscar en claves normalizando
        foreach ($k in $coords.Keys) {
            if ($k -eq $key) { $c = $coords[$k]; break }
        }
    }

    if ($c -ne $null) {
        $latNew = $c.lat
        $lonNew = $c.lon
        # Solo actualizar si la diferencia es significativa (>0.05 grados ~ 5km)
        $diffLat = [Math]::Abs($latOld - $latNew)
        $diffLon = [Math]::Abs($lonOld - $lonNew)
        if ($diffLat -gt 0.05 -or $diffLon -gt 0.05) {
            $viejo = $m.Value
            $nuevo = "$($m.Groups[1].Value)$latNew$($m.Groups[5].Value)$lonNew$($m.Groups[7].Value)"
            $html = $html.Replace($viejo, $nuevo)
            $actualizados++
            Write-Host "  $prov|$mun : ($latOld,$lonOld) -> ($latNew,$lonNew)"
        }
    }
}

